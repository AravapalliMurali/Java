public class operations {
    public static void main(String[] args) {
        int num1 = 4;
        int num2 = 6;
        int result = num1 + num2; // * ,/, %, - operations
        System.out.println(result++);// post increment 
        System.out.println(++result);// pre increment 

        System.out.println(result--);// post decrement 
        System.out.println(--result);// pre decrement 
    }
}
